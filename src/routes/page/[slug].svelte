<script context="module">
  import client from "../../lib/apollo";
  import { PAGE } from "../../queries/page";

  export async function preload({ params, query }) {
    let slug = params.slug;
    return {
      cache: await client.query({
        query: PAGE,
        variables: { slug },
      }),
      slug,
    };
  }
</script>

<script>
  import { restore, query } from "svelte-apollo";

  export let cache;
  export let slug;

  restore(client, PAGE, cache.data);

  const page = query(client, {
    query: PAGE,
    variables: { slug },
  });
</script>

<style lang="scss">
  .special-page-container {
    margin-top: 18rem;
    width: 1366px;
    min-height: 800px;
  }
</style>

<svelte:head>
  <title>Slug</title>
</svelte:head>

{#await cache}
  <p>Loading...</p>
{:then data}
  {#if data.data}
    <div class="app-division-wrapper">
      <div class="section-wrapper">
        <div class="special-page-container">
          <h1 class="app-decor">{data.data.page.title}</h1>
          {@html data.data.page.content}
        </div>
      </div>
    </div>
  {/if}
{/await}
